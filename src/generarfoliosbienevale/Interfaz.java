    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        // TODO add your handling code here
        GenerarFoliosBienevale gfb = new GenerarFoliosBienevale();
        if (!jtf_ruta.getText().isEmpty()) {
            if (camposvacios()) {
                HashMap data = new HashMap<String, String>();
                if (jtf_folini.getText().length() == 9 && jtf_folfin.getText().length() == 9) {
                    data.put("fol_ini", jtf_folini.getText());
                    data.put("fol_fin", jtf_folfin.getText());
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El folio debe de ser de 9 Digitos contando en número de la zona", "Error", JOptionPane.ERROR_MESSAGE);
                }

                data.put("ruta", jtf_ruta.getText());
                data.put("vigencia", jtf_vigencia.getText());
                data.put("serie", jtf_serie.getText());
                data.put("inicia_en", jtf_iniciaen.getText());
                if (jrb_impresion.isSelected()) {
                    System.out.println("impresion");
                    generar.disable();
                    new Thread() {
                        public void run() {
                            gfb.paraImpresion(data, rootPane);
                            iniciarCarga();
                            musarCarga();
                            
                        }
                    }.start();
                    generar.enable();
                } else if (jrb_repos.isSelected()) {
                    System.out.println("repos");
                    generar.disable();
                     new Thread() {
                        public void run() {
                            gfb.paraRepos(data, rootPane);
                            iniciarCarga();
                            musarCarga();
                            }
                    }.start();
                    generar.enable();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ingresa todos los campos para continuar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecciona la ruta de guardado para continuar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_generarActionPerformed

