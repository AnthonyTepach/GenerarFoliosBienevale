private void charTypeString(java.awt.event.KeyEvent ke) {
        char c = ke.getKeyChar();
        if (Character.isDigit(c)) {
            getToolkit().beep();
            ke.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se acceptan letras en este campo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void charTypeInt(java.awt.event.KeyEvent ke) {
        char c = ke.getKeyChar();
        if (Character.isLetter(c)) {
            getToolkit().beep();
            ke.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se acceptan números en este campo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    void iniciarCarga() {
        new Thread() {
            public void run() {
                int a = 0;
                double s = 0;
                while (a <= 101 && s <= 101) {
                    s += 0.30;
                    a += s;
                    jProgressBar1.setValue(a);
                    try {
                        sleep(100);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
        }.start();
    }

    void musarCarga() {
        progreso().setBackground(Color.red);
        progreso().setForeground(new Color(0, 0, 0));
        progreso().setStringPainted(true);
    }

    public javax.swing.JProgressBar progreso() {
        return jProgressBar1;
    }

    private void limitar(JTextField caja, int limite, java.awt.event.KeyEvent ke) {
        if (caja.getText().length() == limite) {
            ke.consume();
        }
    }

    private boolean camposvacios() {
        boolean a;
        a = !(jtf_folfin.getText().isEmpty() && jtf_folini.getText().isEmpty() && jtf_iniciaen.getText().isEmpty() && jtf_serie.getText().isEmpty() && jtf_vigencia.getText().isEmpty());
        return a;
    }
